class AllDataTypes{
	public static void main(String... s){
		Class c = int.class;
		System.out.println(c.getName());

		c = char.class;
		System.out.println(c.getName());

		c = float.class;
		System.out.println(c.getName());

		c = double.class;
		System.out.println(c.getName());

		c = long.class;
		System.out.println(c.getName());

		c = boolean.class;
		System.out.println(c.getName());

		c = byte.class;
		System.out.println(c.getName());

		c = short.class;
		System.out.println(c.getName());

		c = void.class;
		System.out.println(c.getName());
	}
}
